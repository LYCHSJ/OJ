#include<iostream>
using namespace std;
class Solution {
public:
	/**
	* 获得两个整形二进制表达位数不同的数量
	*
	* @param m 整数m
	* @param n 整数n
	* @return 整型
	*/
	int countBitDiff(int m, int n) {
		int ret = m^n;
		int count = 0;
		while (ret) {
			ret &= (ret -1);
			count++;
		}
		return count;
	}
};
int main() {
	Solution s;
	int m = 1999;
	int n = 2299;
	cout << s.countBitDiff(m, n) << endl;
	return 0;
}